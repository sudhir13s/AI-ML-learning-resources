    pred = lda.doc_topic().argmax(axis=1)
    pur = purity(true_topic, pred, 3)
    print(f"\ndoc-topic purity vs planted truth: {pur:.3f}")
    assert pur >= 0.95, "Gibbs LDA failed to recover the planted topics"

    # --- 2. From-scratch NMF on the same corpus ---
    W, H, errors = nmf_multiplicative(counts, n_topics=3, n_iter=400, seed=SEED)
    print("\nNMF (multiplicative updates) recovered topics (top words):")
    for k, words in enumerate(nmf_top_words(H, vocab, n=6)):
        print(f"  topic {k}: {', '.join(words)}")
    print(f"\nNMF Frobenius error: {errors[0]:.2f} -> {errors[-1]:.2f} (monotone decrease)")
    assert errors[-1] < errors[0], "NMF error should decrease"
    assert all(errors[i + 1] <= errors[i] + 1e-6 for i in range(len(errors) - 1)), "NMF must be monotone"

    # --- 3. Coherence: a coherent topic beats a random word set ---
    coherent = [vocab.index(w) for w in PLANTED_TOPIC_WORDS[0]]  # a real planted topic
    rng = np.random.default_rng(SEED)
    random_set = list(rng.choice(V, size=len(coherent), replace=False))
    c_npmi = npmi_coherence(coherent, counts)
    r_npmi = npmi_coherence(random_set, counts)
    c_umass = umass_coherence(coherent, counts)
    r_umass = umass_coherence(random_set, counts)
    print("\ncoherence — coherent (planted) topic vs random word set:")
    print(f"  NPMI : coherent {c_npmi:+.3f}  vs  random {r_npmi:+.3f}")
    print(f"  UMass: coherent {c_umass:+.3f}  vs  random {r_umass:+.3f}")
    assert c_npmi > r_npmi, "a coherent topic must out-score a random set on NPMI"
    assert c_umass > r_umass, "a coherent topic must out-score a random set on UMass"

    # --- 4. scikit-learn end-to-end on the readable corpus ---
    lda_topics, nmf_topics, theta0 = sklearn_lda_nmf(READABLE_CORPUS, n_topics=3)
    print("\nscikit-learn on the 18-doc readable corpus:")
    print("  LDA topics (counts):")
    for k, words in enumerate(lda_topics):
        print(f"    topic {k}: {', '.join(words)}")
    print("  NMF topics (TF-IDF):")
    for k, words in enumerate(nmf_topics):
        print(f"    topic {k}: {', '.join(words)}")
    print(f"  doc-0 topic mixture (theta): {np.round(theta0, 3)}")
    assert theta0.max() > 0.9, "doc-0 (a match report) should be dominated by one topic"
    print("\nall asserts passed.")


